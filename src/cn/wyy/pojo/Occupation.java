package cn.wyy.pojo;

import java.util.Date;

public class Occupation {
	private int occupationId;
	private String occupationName;
	private int occupationMoeny;
	private Date occupationTime;
	public int getOccupationId() {
		return occupationId;
	}
	public void setOccupationId(int occupationId) {
		this.occupationId = occupationId;
	}
	public String getOccupationName() {
		return occupationName;
	}
	public void setOccupationName(String occupationName) {
		this.occupationName = occupationName;
	}
	public int getOccupationMoeny() {
		return occupationMoeny;
	}
	public void setOccupationMoeny(int occupationMoeny) {
		this.occupationMoeny = occupationMoeny;
	}
	public Date getOccupationTime() {
		return occupationTime;
	}
	public void setOccupationTime(Date occupationTime) {
		this.occupationTime = occupationTime;
	}
	
	
}
